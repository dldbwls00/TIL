package prob4;

public abstract class Shape {
	private Shape next;
	public Shape() {
		next = null;
	}
	
	public void setNext(Shape obj) { //링크 연결
		next = obj;
	}
	
	public Shape getNext() {
		return next;
	}
	
	public abstract void draw();
}
